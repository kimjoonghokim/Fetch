import pickle
import argparse
import re
from collections import defaultdict
import sys

# It's assumed that the 'Tree' and 'Node' classes are defined in a way
# that they can be unpickled. We'll create placeholder stubs if needed,
# but pickle should handle the custom classes if they were defined during saving.
from search_tree import Tree, Node

def extract_answer(solution_text):
    """
    Extracts the final numerical answer from a solution string.
    Example: "The answer is 42." -> "42"
    """
    match = re.search(r'The answer is ([\d\.,]+)', solution_text)
    if match:
        answer = match.group(1).replace(',', '')
        return answer
    return None

def main():
    """
    Main function to evaluate the search results.
    """
    parser = argparse.ArgumentParser(
        description="Evaluate search results from a .pkl file."
    )
    parser.add_argument(
        "input_path", 
        type=str, 
        help="Path to the results .pkl file generated by the search script."
    )
    args = parser.parse_args()

    try:
        with open(args.input_path, 'rb') as f:
            data = pickle.load(f)
    except FileNotFoundError:
        print(f"Error: The file '{args.input_path}' was not found.")
        sys.exit(1)
    except pickle.UnpicklingError:
        print(f"Error: The file '{args.input_path}' is not a valid pickle file or is corrupted.")
        sys.exit(1)

    problems = data.get('problems', [])
    metrics = data.get('metrics', {})

    print("--- Search Metrics ---")
    if metrics:
        runtime = metrics.get('total_runtime', 'N/A')
        if isinstance(runtime, float):
            print(f"Total Runtime: {runtime:.2f} seconds")
        else:
            print(f"Total Runtime: {runtime}")
            
        print(f"Total Tokens: {metrics.get('total_tokens', 'N/A')}")
        print(f"  - Prompt Tokens: {metrics.get('total_prompt_tokens', 'N/A')}")
        print(f"  - Completion Tokens: {metrics.get('total_completion_tokens', 'N/A')}")
    else:
        print("No metrics found in the file.")
    print("----------------------\n")

    if not problems:
        print("No problems found in the pickle file.")
        sys.exit(0)

    correct_count = 0
    total_count = len(problems)
    
    for i, problem in enumerate(problems):
        # The ground truth answer
        true_answer = extract_answer(problem.answer) # Assuming problem.answer contains the ground truth
        
        # Find the best solution generated by the model
        # Assuming problem object has a method get_best_solution() that returns a Node or similar object
        best_node = problem.get_best_solution()
        model_answer = None
        if best_node and best_node.content:
            model_answer = extract_answer(best_node.content)

        is_correct = (model_answer is not None and true_answer is not None and model_answer == true_answer)
        if is_correct:
            correct_count += 1
        
        print(f"Problem {i+1}:")
        print(f"  - Ground Truth: {true_answer}")
        print(f"  - Model Answer: {model_answer}")
        print(f"  - Correct: {'Yes' if is_correct else 'No'}")
        print("----------")

    accuracy = (correct_count / total_count) * 100 if total_count > 0 else 0
    print("\n--- Final Results ---")
    print(f"Accuracy: {correct_count} / {total_count} ({accuracy:.2f}%)")
    print("---------------------")

if __name__ == '__main__':
    main()